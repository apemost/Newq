namespace Newq
{
    /// <summary>
    /// Specifies how rows of data are sorted.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// The default. No sort order is specified.
        /// </summary>
        Unspecified,

        /// <summary>
        /// Rows are sorted in ascending order.
        /// </summary>
        Asc,

        /// <summary>
        /// Rows are sorted in descending order.
        /// </summary>
        Desc,
    }
}
